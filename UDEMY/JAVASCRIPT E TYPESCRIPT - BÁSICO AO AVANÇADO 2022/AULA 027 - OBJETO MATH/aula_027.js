// EXEMPLOS DE USO DO OBJETO 'MATH'

let numeroComPontoFlutuante = 152.6546548741;
let arredondaCima = Math.ceil(numeroComPontoFlutuante); // Arredonda para cima
let arredondaBaixo = Math.floor(numeroComPontoFlutuante); // Arredonda para baixo
let maisProximo = Math.round(numeroComPontoFlutuante); // Arredonda para o mais próximo do valor

console.log();
console.log(arredondaCima);
console.log();
console.log(arredondaBaixo);
console.log();
console.log(maisProximo);
console.log();

console.log(Math.max(301, 4454, 6545, 1231, 541, 89)); // Mostra o maior valor da lista
